              1. **Trustworthiness:** Measures the degree to which high-dimensional nearest neighbors are preserved in the 2D projection (prevents false clusters).
              2. **Continuity:** Quantifies how well local structural connections are maintained (prevents cluster fracturing).
              3. **Global Spearman Correlation:** Computes the correlation of all-to-all pairwise distances between the high-dimensional normalized space and the low-dimensional embedding.
