                bslib::accordion(
                    id = ns("multiomics_help_accordion"),
                    multiple = FALSE,
                    
                    bslib::accordion_panel(
                        title = "Protein UMAP x SNV Mapping",
                        icon = shiny::icon("map-marked-alt"),
                        shiny::markdown("
